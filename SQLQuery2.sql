--looking at total cases vs population

select location, date, population, total_cases, (total_cases/population)*100 
from CovidDeaths

---looking at countries with highest infection rate compared to population
select location,population, Max(total_cases) as highest_infection ,Max((total_cases/population)*100) as highest_infection_rate
from CovidDeaths
group by location,population
order by highest_infection_rate DESC

--showing countries with highest death count per population
select location,population, Max(cast(total_deaths as int)) as highest_deaths ,Max((total_deaths/population)*100) as highest_death_rate
from CovidDeaths
where continent is not null
group by location,population
order by highest_deaths DESC

--lets breaks things down by continent
select location,Max(cast(total_deaths as int)) as highest_deaths
from CovidDeaths
where continent is null
group by location
order by highest_deaths DESC

--global numbers

select sum(new_cases) as total_cases, sum(cast(new_deaths as int)) as total_deaths, sum(cast(new_deaths as int))/sum(new_cases) as percentage_death
from CovidDeaths
where continent is not null

